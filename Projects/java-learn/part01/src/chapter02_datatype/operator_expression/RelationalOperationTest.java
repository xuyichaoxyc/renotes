public class RelationalOperationTest {
    public static void main(String[] args) {
        System.out.println("5 > 4 的结果是：" + (5 > 4));
        System.out.println("5 < 4 的结果是：" + (5 < 4));
        System.out.println("5 >= 4 的结果是：" + (5 >= 4));
        System.out.println("5 <= 4 的结果是：" + (5 <= 4));
        System.out.println("5 != 4 的结果是：" + (5 != 4));
        System.out.println("5 == 4 的结果是：" + (5 == 4));
    }
}
