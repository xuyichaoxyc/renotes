public class FloatTest {
    public static void main(String[] args) {
        // float：4个字节
        float f;
        // double：8个字节
        double d;

        f = 12.11f;
        d = 1.001;

        System.out.println("float 类型变量 f 的值为：" + f);
        System.out.println("double 类型变量 d 的值为：" + d);
    }
}
